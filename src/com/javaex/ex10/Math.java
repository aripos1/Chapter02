package com.javaex.ex10;

public class Math {

	public static int plus(int a, int b) {

		int sum = a + b;
		return sum;

	}

	public static double plus(double a, int b) {

		double sum = a + b;
		return sum;

	}

	public static double plus(int a, double b) {

		double sum = a + b;
		return sum;

	}

	public static double plus(double a, double b) {

		double sum = a + b;
		return sum;

	}
}
